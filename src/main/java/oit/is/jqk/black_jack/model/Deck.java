package oit.is.jqk.black_jack.model;

public class Deck {
  int deck_id;
  int deal_card;
  int id;

  public int getDeck_id() {
    return deck_id;
  }

  public void setDeck_id(int deck_id) {
    this.deck_id = deck_id;
  }

  public int getDeal_card() {
    return deal_card;
  }

  public void setDeal_card(int deal_card) {
    this.deal_card = deal_card;
  }

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

}
