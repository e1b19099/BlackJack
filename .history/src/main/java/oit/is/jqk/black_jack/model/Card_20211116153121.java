public class Card {
    int id;
    String suit;
    int number;

    public String getSuit() {
        return suit;
      }
    
      public void setSuit(String suit) {
        this.suit = suit;
      }
    
      public int getNumber() {
        return number;
      }
    
      public void setNumber(int number) {
        this.number = number;
      }
    
      public int getId() {
        return id;
      }
    
      public void setId(int id) {
        this.id = id;
      }
}